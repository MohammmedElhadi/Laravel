    /**
     * mark notification as read.
     *
     * @return \Illuminate\Http\Response
     */
    public function markAsRead($id)
    {
       $notification = Auth::user()->notifications()->where('id' , $id)->get();
       $notification->markAsRead();
       return response()->json([], 200);
    }




